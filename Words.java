class Words{
  private String type;
  private String name;

  public Words(String type, String name){
    this.type=type;
    this.name=name;
  }
  public String getType() {
        return type;
    }
  public void setType(String t) {
        this.type=t;
    }
  public String getName() {
        return name;
    }
    
}